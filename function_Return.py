"""
User Defined Functions with Return Values
"""

# User Defined Function without input and with return value

def mul():
    a=10
    b=26
    c=a*b
    return c

res=mul()
print(res)

def add():
    v=123
    k=259
    f=v+k
    return f
ty=add()
print(ty)

# User Defined Function with input and return values

def dif(x,y):
    c=x-y
    return c
res=dif(19,6)
print(res)

def div(h,j):
    l=h/j
    return l
ui=div(12,3)
print(ui)